a = 8
b = 2

print('Addition:\t\t' , a, '+', b, '=', a + b)

print('Subtraction:\t\t' , a, '-', b, '=', a - b)

print('Multiplication:\t\t' , a, '*', b, '=', a * b)

print('Division:\t\t' , a, '/', b, '=', a / b)
print('Floor Division:\t\t' , a, '/', b, '=', a // b)

print('Remainder:\t\t' , a, '%', b, '=', a % b)

print('Exponent:\t\t' , a, '**', b, '=', a ** b, sep = '')
